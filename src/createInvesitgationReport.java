    public void Show(int id){
        matters1.setText(txt1);
        matters2.setText(txt2);
        facts.setText(txt3);
        findings.setText(txt4);
        idTxt.setText(String.valueOf(id));
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/crime record", "root", "");
            PreparedStatement pst = con.prepareStatement("select * from report  where id=?");
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                name=rs.getString("victim");
                nameTxt.setText(name);
                age = rs.getString("victimAge");
                ageTxt.setText(age);
                profession = rs.getString("profession");
                profTxt.setText(profession);
                type = rs.getString("crimeType");
                crimeTypeTxt.setText(type);
                defaultFactsText=rs.getString("description");
                if(rs.getString("witnessPresent").equals("y"))
                    defaultFactsText+="\n An witness is present in the case, named " + rs.getString("witness")+ "." + "\n" + "Witness states that " + rs.getString("witnessStatement") + ".";
                
                imageLocation = rs.getString("victimImage");
                
                Image getAbsolutePath = null;
                ImageIcon icon = new ImageIcon(imageLocation);
                Image image = icon.getImage().getScaledInstance(victimImageLabel.getWidth(), victimImageLabel.getHeight(), Image.SCALE_SMOOTH);
                victimImageLabel.setIcon(icon);
            }
        } catch (Exception e) {
        }
        
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/crime record", "root", "");
            PreparedStatement pst = con.prepareStatement("select * from investigation  where id=?");
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                defaultInvestigationText = "Suspect was located from " + rs.getString("locatedFrom") + " and arrested by " + rs.getString("arrestedBy") + ". Suspect is currently under " + rs.getString("currentlyUnder") +".";
            }
        } catch (Exception e) {
        }
    }
