    private void submitPanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitPanelMouseClicked
        submitText.setForeground(new java.awt.Color(255,255,255));
        
        if(mattersToBeInvestigatedTxt.getText().length()!=0 && factsOfTheCaseTxt.getText().length()!=0 && investigationFindingsTxt.getText().length()!=0 &&  mattersToBeInvestigatedTxt.isVisible() && factsOfTheCaseTxt.isVisible() && investigationFindingsTxt.isVisible()){
            mattersToBeInvestigated=mattersToBeInvestigatedTxt.getText();
            factsOfTheCase=factsOfTheCaseTxt.getText();
            invesitgationFindings=investigationFindingsTxt.getText();
           
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/crime record", "root", "");
                PreparedStatement pst = con.prepareStatement("insert into investigationreport values(?,?,?,?,?,?,?,?,?)");

                pst.setInt(1, id);
                pst.setString(2, name);
                pst.setString(3, age);
                pst.setString(4, profession);
                pst.setString(5, type);
                pst.setString(6, imageLocation);
                pst.setString(7, mattersToBeInvestigated);
                pst.setString(8, factsOfTheCase);
                pst.setString(9,invesitgationFindings);
                pst.executeUpdate();
            } catch (Exception e) {
            }
        
            String str="y";
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/crime record", "root", "");
                Statement s = con.createStatement();

                s.executeUpdate("update investigation set reportCreated='"+str+"' where id ='"+id+"' ");
                JOptionPane.showMessageDialog(null, "Investigation report on case id " + id + " has been created successfully");
            } catch (Exception e) {
            }
        }   
        else
            JOptionPane.showMessageDialog(null, "Invalid Info Provided");  
       
    }//GEN-LAST:event_submitPanelMouseClicked

