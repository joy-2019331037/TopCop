    private void idTxtFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_idTxtFocusGained
        if(idTxt.getText().equals("Enter ID")){
           idTxt.setText("");
           removePlaceHolder(idTxt);
       }
    }//GEN-LAST:event_idTxtFocusGained

    private void idTxtFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_idTxtFocusLost
       if(idTxt.getText().equals("")){
           addPlaceHolder(idTxt);
           idTxt.setText("Enter ID");
       }
    }//GEN-LAST:event_idTxtFocusLost

    private void passwordTxtFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordTxtFocusGained
       if(passwordTxt.getText().equals("Enter Password")){
          passwordTxt.setText("");
          passwordTxt.setEchoChar('*');
          removePlaceHolder(passwordTxt);
       }
    }//GEN-LAST:event_passwordTxtFocusGained

    private void passwordTxtFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordTxtFocusLost
       if(passwordTxt.getText().equals("")){
           addPlaceHolder(passwordTxt);
          passwordTxt.setText("Enter Password");
          passwordTxt.setEchoChar('\u0000');
       }
    }//GEN-LAST:event_passwordTxtFocusLost

