    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed
       if(idTxt.getText().length()==0 || passwordTxt.getText().length()==0){
           JOptionPane.showMessageDialog(this, "ID or Password is Blank.");
       }
        else{
             try {
                String id = idTxt.getText();
                String password = passwordTxt.getText();


                Class.forName("com.mysql.cj.jdbc.Driver");
                con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/crime record", "root", "");
                pst = con.prepareStatement("select * from admin where id = ? and password=? ");
                pst.setString(1, id);
                pst.setString(2, password);
                rs = pst.executeQuery();
            
                if(rs.next()){
                    AdminWindow aw = new AdminWindow();
                    this.dispose();
                    aw.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this, "ID or Password did not match.\nTry again.");
                    idTxt.setText("");
                    passwordTxt.setText("");
                    idTxt.requestFocus();
                }
            
       } catch (ClassNotFoundException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
       
    }//GEN-LAST:event_kButton1ActionPerformed

