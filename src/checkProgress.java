    private void progressButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_progressButtonActionPerformed
        String ID =reportIDtxt.getText();
        int id = Integer.parseInt(ID);
        
        
        int percantage=0;
        percantageLabel.setText("Investigation on your report has not been processed yet.");
        //we will check the progress table for showing a percantage 
        try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/crime record", "root", "");
                PreparedStatement pst = con.prepareStatement("select id,investigationStarted,createFIR,evidenceCollected,"
                        + "suspectLocated,victimFound,suspectConfessed,userid from progress where id=? ");
                
                pst.setInt(1, id);
                ResultSet rs=pst.executeQuery();
                
                //here no need to check for if currentUserId==reportFiledbyID or not
                //beacuse this will already be checked by the search button action
                //and the progress button wil only be activated if currentUserId==reportFiledbyID is verified from search button action performed
                while(rs.next()){
                    if(rs.getString("investigationStarted").equals("y")){
                        percantage=20;
                        percantageLabel.setText("Investigation on your report is completed by 20%.");
                    }
                    if(rs.getString("createFIR").equals("y")){
                        percantage=40;
                        percantageLabel.setText("Investigation on your report is completed by 40%");
                    }
                    if(rs.getString("evidenceCollected").equals("y")){
                        percantage=50;
                        percantageLabel.setText("Investigation on your report is completed by 50%");
                    }
                    if(rs.getString("suspectLocated").equals("y")){
                        percantage=75;
                        percantageLabel.setText("Investigation on your report is completed by 75%");
                    }
                    if(rs.getString("VictimFound").equals("y")){
                        percantage=90;
                        percantageLabel.setText("Investigation on your report is completed by 90%");
                    }
                    if(rs.getString("suspectConfessed").equals("y")){
                        percantage=100;
                        percantageLabel.setText("Investigation on your report is completed.");
                        seeDetailedReportPanel.setVisible(true);
                    }
                }
        } catch (Exception e) {
        }
        
        final int p = percantage;
        
        new Thread(new Runnable() {
             @Override
             public void run() {
                 progressPanel.setVisible(true);
                 for(int i=1;i<=p;i++){
             
                    try {
                        progressPanel.updateProgress(i);
                        progressPanel.repaint();
                        Thread.sleep(10);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(checkProgress.class.getName()).log(Level.SEVERE, null, ex);
                    }
              
                }
            }
         }).start();
        
        percantageLabel.setVisible(true);
        
        //bringing progress info from database table progress
        ShowProgress(id);
   
        progressButton.setEnabled(false);
    }//GEN-LAST:event_progressButtonActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/crime record", "root", "");
            PreparedStatement pst = con.prepareStatement("select * from report where id=? ");
            
            int id = Integer.parseInt(reportIDtxt.getText());
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
                
           if(rs.next()==false){
               //means this record is not present in the entire database
              notFoundLabel.setVisible(true);
              progressButton.setEnabled(false);
              progressPanel.setVisible(false);
              
              victimTxt.setText("");
              crimeTypeTxt.setText("");
              dateTxt.setText("");
              }
           else{
                //means this record is present in the database
               
              
               
               int currentUserId=UserWindow.userid;
               int reportFiledById=Integer.parseInt(rs.getString("userid"));
               
               //this is for preventing one user to see other users' report
               if(currentUserId==reportFiledById){
                   //the record is present in the database
                   //and the record being searched was also filed by the searcher
                   notFoundLabel.setVisible(false);
                   progressButton.setEnabled(true);
                   progressPanel.setVisible(false);
                   
                    String v = rs.getString("victim");
                    victimTxt.setText(v);
                    victimTxt.setEditable(false);
                    String type = rs.getString("crimeType");
                    crimeTypeTxt.setText(type);
                    crimeTypeTxt.setEditable(false);
                    dateTxt.setText(rs.getString("reportFiledOn"));
                    dateTxt.setEditable(false);
               }
               else{
                   //the record is present in the database
                   //but the record being searched was not filed by the searcher
                    notFoundLabel.setVisible(true);
                    progressButton.setEnabled(false);
                    progressPanel.setVisible(false);
                    
                    victimTxt.setText("");
                    crimeTypeTxt.setText("");
                    dateTxt.setText("");
               }
               
           }           
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_searchButtonActionPerformed

