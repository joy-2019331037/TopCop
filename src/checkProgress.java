    public void ShowProgress(int id){
         try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/crime record", "root", "");
                PreparedStatement pst = con.prepareStatement("select id,investigationStarted,createFIR,evidenceCollected,"
                        + "suspectLocated,suspectConfessed,victimFound,userid,consentRequested,consentGiven from progress where id=?");
                
                pst.setInt(1, id);
                ResultSet rs=pst.executeQuery();
                
               while(rs.next()){
                    if(rs.getString("investigationStarted").equals("y")){
                        investigationLabel.setVisible(true);
                    }
                    
                    if(rs.getString("createFIR").equals("y")){
                        FIRLabel.setVisible(true);
                        seeFIRcopyLabel.setVisible(true);
                        seeFIRcopyLabel.setText(hyprlinkText);
                        seeFIRcopyLabel.setForeground(new Color(51,51,255));
                    }
                    if(rs.getString("evidenceCollected").equals("y")){
                        evidenceLabel.setVisible(true);
                    }
                    if(rs.getString("suspectLocated").equals("y")){
                        suspectLocatedLabel.setVisible(true);
                    } 
                    if(rs.getString("suspectconfessed").equals("y")){
                        suspectConfessedLabel.setVisible(true);
                    } 
                    if(rs.getString("victimFound").equals("y")){
                        victimFoundLabel.setVisible(true);
                    }
                    if(rs.getString("consentRequested").equals("y")){
                        if(rs.getString("consentGiven").equals("n")){
                            int x = JOptionPane.showConfirmDialog(null, "Admin requests your consent for creating FIR on this report.\n"
                                + "Do you give your consent?", "Select", JOptionPane.YES_NO_OPTION);
                            if(x==0){
                                try {
                                    String s="y";
                                    Statement st = con.createStatement();

                                    st.executeUpdate("  UPDATE progress SET consentGiven='"+s+"' WHERE id='"+id+"'   ");
                                    JOptionPane.showMessageDialog(this, "Consent for creating FIR has been provided to the admin.\nYou will soon have an update.");
                                    } catch (Exception e) {
                                }
                            }
                        }
                    }
                   
                }    
                  
                } catch (Exception e) {
                }
         
    }
